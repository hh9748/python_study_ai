"""
1. 在数学中，像 1 + 1 = 2 这样的等式表示：等号左边的 1 + 1 是具体的运算过程，等号右边
的 2 是该运算的结果。
2. 在代码 age = 18 中，等号表示：将等号右侧的值与左侧的变量建立绑定关系。因此，当程
序中需要表示年龄 18 时，可以使用变量 age；同样，也可以通过 age 来修改该数值。
3. age = 18 这一行代码也被称为“赋值语句”，意思是将右侧的 18 赋给变量 age。
4. 在 Python 中，变量的创建与赋值是同时完成的。也就是说，当程序中出现一个变量时，它
必须立即与某个值建立绑定关系。
5. 变量名不应过于随意，命名时需要遵守一定的规则（具体命名规则将在下一小节讲解）。
"""
name = '张三'
weight = 65.2

print(name + '的体重是', weight)
print('对于', weight, '这个体重，张三觉得不满意')
print('张三决定开始减肥，希望体重比', weight, '还要轻')
